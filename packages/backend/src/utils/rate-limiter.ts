import rateLimit from 'express-rate-limit';

/**
 * Rate limiter for general API endpoints
 * Allows 100 requests per 15 minutes
 */
export const apiLimiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100, // Limit each IP to 100 requests per windowMs
  message: 'Too many requests from this IP, please try again later.',
  standardHeaders: true, // Return rate limit info in the `RateLimit-*` headers
  legacyHeaders: false, // Disable the `X-RateLimit-*` headers
});

/**
 * Stricter rate limiter for proof operations
 * Allows 20 requests per 15 minutes
 */
export const proofLimiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 20, // Limit each IP to 20 requests per windowMs
  message: 'Too many proof operations from this IP, please try again later.',
  standardHeaders: true,
  legacyHeaders: false,
});
